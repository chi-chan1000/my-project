void draw_maze() {
    int i, j;
    for(i = 0; i < 80; i++){
        for(j = 0; j < 80; j++){
            if(maze[j][i] == 1){
                draw_point(i+80 , j+80, WHITE);
            }else if(maze[j][i] == 3){
                draw_point(i+80, j+80, GREEN);
            }else{
                draw_point(i+80, j+80, BLACK);
            }
        }
    }
}


void move_player(int dx, int dy) {
    int new_x = player_x + dx;
    int new_y = player_y + dy;
    
    if (maze[new_y][new_x] == 1) { // 次の位置が白なら移動可能
        draw_point(player_x + 80, player_y + 80, WHITE); // 元の位置を白に戻す
        player_x = new_x;
        player_y = new_y;
        draw_point(player_x + 80, player_y + 80, RED); // 新しい位置を赤くする
    }else if (maze[new_y][new_x] == 3) { // ゴールに到達した場合
        draw_point(player_x + 80, player_y + 80, WHITE); // 元の位置を白に戻す
        player_x = new_x;
        player_y = new_y;
        draw_point(player_x + 80, player_y + 80, GREEN); // ゴール位置を緑のままにする

        *((unsigned short *)0x04000102) = 0x0000;
        
        locate(7, 8);
        prints("Congratulation!");
    }

}
